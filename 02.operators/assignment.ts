// Log the value after each step.

let score = 50;

score += 20; // Add 20
console.log("After adding 20:", score);

score -= 10; // Subtract 10
console.log("After subtracting 10:", score);

score *= 2; // Multiply by 2
console.log("After multiplying by 2:", score);

score /= 4; // Divide by 4
console.log("After dividing by 4:", score);


